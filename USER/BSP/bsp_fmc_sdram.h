#define _BSP_FMC_SDRAM_H

#include "stm32f4xx.h"

#define EXT_SDRAM_ADDR  	((uint32_t)0xC0000000)
#define EXT_SDRAM_SIZE		(16 * 1024 * 1024)


/*
**********************************************************************************************************
						   LCD显存使用，共使用8MB，SDRAM容量16MB
**********************************************************************************************************
*/
