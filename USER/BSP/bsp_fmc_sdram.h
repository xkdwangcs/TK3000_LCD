/* LCD显存, 图层2, 分配4M字节 */
#define SDRAM_LCD_BUF2	   (EXT_SDRAM_ADDR + 4 * 1024 * 1024)

/* 仅SDRAM驱动里面的测试代码调用了 */
#define SDRAM_APP_SIZE	   (8 * 1024 * 1024)

/*
**********************************************************************************************************
					emWin动态内存使用，除了显存使用的8MB，后8MB给动态内存使用
**********************************************************************************************************
*/
