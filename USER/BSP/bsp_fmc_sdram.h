//emWin动态内存使用，除了显存使用的8MB，后7MB给动态内存使用
#define SDRAM_APP_BUF     (EXT_SDRAM_ADDR + 9 * 1024 * 1024)  	//emWin动态内存首地址//8

void bsp_InitExtSDRAM(void);
